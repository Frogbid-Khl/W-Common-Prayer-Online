        <div class="col-12 pt-5 text-center">
            <h1 class="cpo-content-page-title">THE LITANY</h1>
            <p>As Written in the 1928 Book of Common Prayer</p>
        </div>
        <div class="col-lg-12 text-center mt-3">
            <p>Developed and Presented by</p>
            <h3>www.CommonPrayerOnline.org</h3>
            <p>An Independent, Traditional</p>
            <p>1928 BCP Ministry</p>
        </div>
        <div class="col-lg-12 mt-3">
            <div>
                <span>Aa</span>
                <input type="range" id="fontSizeRange" class="cpo-range" min="10" max="40" step="1" value="16"/>
                <span>Aa</span>
            </div>
        </div>
        <div class="col-lg-12 mt-3">
            <blockquote id="text-container">

                <small>
                    <i>To be used after the Third Collect at Morning or Evening Prayer; or before the
                        Holy Communion; or separately.</i><br/><br/>
                </small>
                <blockquote>

                    <b>
                        O GOD the Father, Creator of heaven and earth;<br/>
                        <i>Have mercy upon us.</i><br/>
                        O God the Son, Redeemer of the world;<br/>
                        <i>Have mercy upon us.</i><br/>
                        O God the Holy Ghost, Sanctifier of the faithful;<br/>
                        <i>Have mercy upon us.</i><br/>
                        O holy, blessed, and glorious Trinity, one God;<br/>
                        <i>Have mercy upon us.</i><br/>
                        <br/>


                        REMEMBER not, Lord, our offences, nor the offences of
                        our forefathers; neither take thou vengeance of our sins:
                        Spare us, good Lord, spare thy people, whom thou hast
                        redeemed with thy most precious blood, and be not angry
                        with us for ever.<br/>
                        <i>Spare us, good Lord.</i><br/>
                        <br/>


                        From all evil and mischief; from sin; from the crafts and
                        assaults of the devil; from thy wrath, and from
                        everlasting damnation,<br/>
                        <i>Good Lord, deliver us.</i><br/>
                        From all blindness of heart; from pride, vainglory, and
                        hypocrisy; from envy, hatred, and malice, and all
                        uncharitableness,<br/>
                        <i>Good Lord, deliver us.</i><br/>
                        From all inordinate and sinful affections; and from all the
                        deceits of the world, the flesh, and the devil,<br/>
                        <i>Good Lord, deliver us.</i><br/>
                        From lightning and tempest; from earthquake, fire, and
                        flood; from plague, pestilence, and famine; from battle
                        and murder, and from sudden death,<br/>
                        <i>Good Lord, deliver us.</i><br/>
                        From all sedition, privy conspiracy, and rebellion; from
                        all false doctrine, heresy, and schism; from hardness of
                        heart, and contempt of thy Word and Commandment,<br/>
                        <i>Good Lord, deliver us.</i><br/>
                        By the mystery of thy holy Incarnation; by thy holy
                        Nativity and Circumcision; by thy Baptism, Fasting, and
                        Temptation,<br/>
                        <i>Good Lord, deliver us.</i><br/>
                        <br/>

                        By thine Agony and Bloody Sweat; by thy Cross and
                        Passion; by thy precious Death and Burial; by thy
                        glorious Resurrection and Ascension, and by the Coming
                        of the Holy Ghost,<br/>
                        <i>Good Lord, deliver us.</i><br/>
                        In all time of our tribulation; in all time of our prosperity;
                        in the hour of death, and in the day of judgment,<br/>
                        <i>Good Lord, deliver us.</i><br/>
                        <br/>

                        WE sinners do beseech thee to hear us, O Lord God; and
                        that it may please thee to rule and govern thy holy
                        Church universal in the right way;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee so to rule the heart of thy
                        servant, The President of the United States, that he may
                        above all things seek thy honour and glory;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to bless and preserve all
                        Christian Rulers and Magistrates, giving them grace to
                        execute justice, and to maintain truth;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to illuminate all Bishops, Priests,
                        and Deacons, with true knowledge and understanding of
                        thy Word; and that both by their preaching and living
                        they may set it forth, and show it accordingly;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to send forth labourers into thy
                        harvest;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to bless and keep all thy people;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to give to all nations unity,
                        peace, and concord;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to give us an heart to love and
                        fear thee, and diligently to live after thy commandments;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to give to all thy people increase
                        of grace to hear meekly thy Word, and to receive it with
                        pure affection, and to bring forth the fruits of the Spirit;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to bring into the way of truth all
                        such as have erred, and are deceived;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to strengthen such as do stand;
                        and to comfort and help the weak-hearted; and to raise
                        up those who fall; and finally to beat down Satan under
                        our feet;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to succour, help, and comfort, all
                        who are in danger, necessity, and tribulation;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        <br/>

                        That it may please thee to preserve all who travel by
                        land, by water, or by air, all women in child-birth, all sick
                        persons, and young children; and to show thy pity upon
                        all prisoners and captives;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to defend, and provide for, the
                        fatherless children, and widows, and all who are desolate
                        and oppressed;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to have mercy upon all men;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to forgive our enemies,
                        persecutors, and slanderers, and to turn their hearts;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to give and preserve to our use
                        the kindly fruits of the earth, so that in due time we may
                        enjoy them;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        That it may please thee to give us true repentance; to
                        forgive us all our sins, negligences, and ignorances; and
                        to endue us with the grace of thy Holy Spirit to amend
                        our lives according to thy holy Word;<br/>
                        <i>We beseech thee to hear us, good Lord.</i><br/>
                        <br/>

                        Son of God, we beseech thee to hear us.<br/>
                        <i>Son of God, we beseech thee to hear us.</i><br/>
                        O Lamb of God, who takest away the sins of the world;<br/>
                        <i>Grant us thy peace.</i><br/>
                        O Lamb of God, who takest away the sins of the world;<br/>
                        <i>Have mercy upon us.</i><br/>
                        O Christ, hear us.<br/>
                        <i>O Christ, hear us.</i><br/>
                        Lord, have mercy upon us.<br/>
                        <i>Lord, have mercy upon us.</i><br/>
                        Christ, have mercy upon us.<br/>
                        <i>Christ, have mercy upon us.</i><br/>
                        Lord, have mercy upon us.<br/>
                        <i>Lord, have mercy upon us.</i><br/>
                        <br/>
                    </b>
                </blockquote>

                <small>
                    <br/><i>Then shall the Minister, and the People with him, say the Lord's Prayer.</i><br/><br/>
                    <br/>
                </small>

                <b>
                    OUR Father, who art in heaven, Hallowed be thy Name.
                    Thy kingdom come.  Thy will be done, On earth as it is in
                    heaven.  Give us this day our daily bread.  And forgive us
                    our trespasses, As we forgive those who trespass against
                    us.  And lead us not into temptation, But deliver us from
                    evil.  Amen.<br/>
                    <br/>
                </b>


                <small>
                    <i>The Minister may, at his discretion, omit all that followeth, to the Prayer,</i> We
                    humbly beseech thee, O Father,<i> etc.</i><br/>
                    <br/>
                    <br/>
                </small>

                <div class="text-center">
                    <small>
                        <i>Minister.</i>
                    </small>
                </div><br/>

                <div class="text-center">
                    <b>
                        O LORD, deal not with us according to our sins.<br/>
                        <i>Neither reward us according to our iniquities.</i><br/>
                    </b>
                </div>
                <br/>

                <div class="text-center"><b>Let us pray.</b></div><br/>

                <b>
                    O GOD, merciful Father, who despisest not the sighing of
                    a contrite heart, nor the desire of such as are sorrowful;
                    Mercifully assist our prayers which we make before thee
                    in all our troubles and adversities, whensoever they
                    oppress us; and graciously hear us, that those evils which
                    the craft and subtilty of the devil or man worketh against
                    us, may, by thy good providence, be brought to nought;
                    that we thy servants, being hurt by no persecutions, may
                    evermore give thanks unto thee in thy holy Church;
                    through Jesus Christ our Lord.  <i>Amen.</i><br/>
                    <br/>
                </b>
                <br/>

                <div class="text-center">
                    <small>
                        <i>Minister and People.</i>
                    </small>
                </div>
                <br/>

                <div class="text-center">
                    <b>
                        O Lord, arise, help us, and deliver us for
                        thy Name's sake.<br/>
                    </b>
                </div>
                <br/>
                <br/>

                <div class="text-center">
                    <small>
                        <i>Minister.</i>
                    </small>
                </div>
                <br/>

                <b>
                    O GOD, we have heard with our ears, and our fathers
                    have declared unto us, the noble works that thou didst in
                    their days, and in the old time before them.<br/>
                </b>
                <br/>
                <br/>

                <div class="text-center">
                    <small>
                        <i>Minister and People.</i>
                    </small>
                </div>
                <br/>

                <div class="text-center">
                    <b>
                        O Lord, arise, help us, and deliver us for thine honour.<br/>
                    </b>
                </div>
                <br/>

                <div class="text-center">
                    <small>
                        <i>Minister.</i>
                    </small>
                </div>
                <br/>

                <blockquote>
                    <b>
                        Glory be to the Father, and to the Son, and to
                        the Holy Ghost;<br/>
                        <i>As it was in the beginning, is now, and ever shall be,
                            world without end.  Amen.</i><br/>
                        <br/>

                        From our enemies defend us, O Christ.<br/>
                        <i>Graciously look upon our afflictions.</i><br/>
                        With pity behold the sorrows of our hearts.<br/>
                        <i>Mercifully forgive the sins of thy people.</i><br/>
                        Favourably with mercy hear our prayers.<br/>
                        <i>O Son of David, have mercy upon us.</i><br/>
                        <br/>

                        Both now and ever vouchsafe to hear us, O Christ.<br/>
                        <i>Graciously hear us, O Christ;
                            graciously hear us, O Lord Christ.</i><br/>
                        <br/>

                        O Lord, let thy mercy be showed upon us;<br/>
                        <i>As we do put our trust in thee.</i><br/>
                        <br/>
                    </b>
                </blockquote>


                <div class="text-center">
                    <b>
                        Let us pray.
                    </b>
                </div><br/>

                <b>
                    We humbly beseech thee, O Father, mercifully to look
                    upon our infirmities; and, for the glory of thy Name, turn
                    from us all those evils that we most justly have deserved;
                    and grant, that in all our troubles we may put our whole
                    trust and confidence in thy mercy, and evermore serve
                    thee in holiness and pureness of living, to thy honour and
                    glory; through our only Mediator and Advocate, Jesus
                    Christ our Lord.  <i>Amen.</i><br/><br/>
                    <br/>
                </b>

                <small>
                    <br/><i>The Minister may end the Litany here, or at his discretion add other
                    <a href="#"><b>Prayers</b></a>
                    from the Book.</i><br/>
                    <br/>
                </small>

            </blockquote>
        </div>
