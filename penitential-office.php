        <div class="col-lg-12 mt-3">
            <div>
                <span>Aa</span>
                <input type="range" id="fontSizeRange" class="cpo-range" min="10" max="40" step="1" value="16"/>
                <span>Aa</span>
            </div>
        </div>
        <div class="col-lg-12 mt-3">
            <blockquote id="text-container">

                <small>
                    <i>On the First Day of Lent, the Office ensuing may be read immediately after the Prayer,
                    </i>We humbly beseech thee, O Father,<i> in the Litany; or it may be used with
                    Morning Prayer, or Evening Prayer, or as a separate Office.<br/>
                    <br/>

                    The same Office may be read at other times, at the discretion of the Minister.<br/>
                    <br/>

                    The Minister and the People kneeling, then shall be said by them this
                    Psalm following:<br/><br/></i>
                </small>

                <div class="text-center"><i><b>Miserere mei, Deus. </b></i>Psalm li.</div>
                <br/>
                <br/>

                <b>
                    HAVE mercy upon me, O God, after thy great goodness;
                    * according to the multitude of thy mercies do away
                    mine offences.<br/>
                    <small>&nbsp; <br/></small>
                    Wash me throughly from my wickedness,
                    * and cleanse me from my sin.<br/>
                    <small>&nbsp; <br/></small>
                    For I acknowledge my faults,
                    * and my sin is ever before me.<br/>
                    <small>&nbsp; <br/></small>
                    Against thee only have I sinned, and done this
                    evil in thy sight;
                    * that thou mightest be justified in thy saying,
                    and clear when thou shalt judge.<br/>
                    <small>&nbsp; <br/></small>
                    Behold, I was shapen in wickedness,
                    * and in sin hath my mother conceived me.<br/>
                    <small>&nbsp; <br/></small>
                    But lo, thou requirest truth in the inward parts,
                    * and shalt make me to understand wisdom secretly.<br/>
                    <small>&nbsp; <br/></small>
                    Thou shalt purge me with hyssop, and I shall be clean;
                    * thou shalt wash me, and I shall be whiter than snow.<br/>
                    <small>&nbsp; <br/></small>
                    Thou shalt make me hear of joy and gladness,
                    * that the bones which thou hast broken may rejoice.<br/>
                    <small>&nbsp; <br/></small>
                    Turn thy face from my sins,
                    * and put out all my misdeeds.<br/>
                    <small>&nbsp; <br/></small>
                    Make me a clean heart, O God,
                    * and renew a right spirit within me.<br/>
                    <small>&nbsp; <br/></small>
                    Cast me not away from thy presence,
                    * and take not thy holy Spirit from me.<br/>
                    <small>&nbsp; <br/></small>
                    O give me the comfort of thy help again,
                    * and stablish me with thy free Spirit.<br/>
                    <small>&nbsp; <br/></small>
                    Then shall I teach thy ways unto the wicked,
                    * and sinners shall be converted unto thee.<br/>
                    <small>&nbsp; <br/></small>
                    Deliver me from blood-guiltiness, O God,
                    thou that art the God of my health;
                    * and my tongue shall sing of thy righteousness.<br/>
                    <small>&nbsp; <br/></small>
                    Thou shalt open my lips, O Lord,
                    * and my mouth shall show thy praise.<br/>
                    <small>&nbsp; <br/></small>
                    For thou desirest no sacrifice, else would I give it thee;
                    * but thou delightest not in burnt-offerings.<br/>
                    <small>&nbsp; <br/></small>
                    The sacrifice of God is a troubled spirit:
                    * a broken and contrite heart, O God, shalt thou not despise.<br/>
                    <small>&nbsp; <br/></small>
                    O be favourable and gracious unto Sion;
                    * build thou the walls of Jerusalem.<br/>
                    <small>&nbsp; <br/></small>
                    Then shalt thou be pleased with the sacrifice of
                    righteousness, with the burnt-offerings and oblations;
                    * then shall they offer young bullocks upon thine altar.<br/>
                    <small>&nbsp; <br/></small>
                    Glory be to the Father, and to the Son, * and to the Holy Ghost;<br/>
                    <small>&nbsp; <br/></small>
                    As it was in the beginning, is now, and ever shall be, * world
                    without end.  Amen.<br/><br/>
                    <br/>
                    <br/>
                </b>

                <small>
                    <i>If the Litany hath been already said, the Minister may pass at once to</i>
                    O Lord, save thy servants;<i>etc.</i><br/>
                    <br/>
                </small>

                <blockquote>
                    <blockquote>
                        <b>
                            Lord, have mercy upon us.<br/>
                            <i>Christ, have mercy upon us.</i><br/>
                            Lord, have mercy upon us.<br/><br/>
                        </b>
                    </blockquote>
                </blockquote>


                <b>
                    OUR Father, who art in heaven, Hallowed be thy Name.  Thy kingdom come.
                    Thy will be done, On earth as it is in heaven.  Give us this day our daily
                    bread.  And forgive us our trespasses, As we forgive those who trespass
                    against us.  And lead us not into temptation, But deliver us from evil.  Amen.
                    <br/>
                    <br/>
                </b>


                <blockquote>
                    <blockquote>
                        <b>
                            O Lord, save thy servants;<br/>
                            <i>That put their trust in thee.</i><br/>
                            Send unto them help from above.<br/>
                            <i>And evermore mightily defend them.</i><br/>
                            Help us, O God our Saviour.<br/>
                            <i>And for the glory of thy Name deliver us;<br/>
                                &nbsp; &nbsp; &nbsp; be merciful to us sinners,for thy Name's sake.</i><br/>
                            O Lord, hear our prayer.<br/>
                            <i>And let our cry come unto thee.</i>
                        </b>
                        <br/>
                        <br/>
                    </blockquote>
                </blockquote>


                <div class="text-center"><b>Let us pray.</b></div>
                <br/>
                <br/>

                <b>
                    O LORD, we beseech thee, mercifully hear our prayers, and spare all those
                    who confess their sins unto thee; that they, whose consciences by sin are
                    accused, by thy merciful pardon may be absolved; through Christ our Lord.
                    <i>Amen.</i>
                    <br/>
                    <br/>

                    O MOST mighty God, and merciful Father, who hast compassion upon all men,
                    and who wouldest not the death of a sinner, but rather that he should turn
                    from his sin, and be saved; Mercifully forgive us our trespasses; receive and
                    comfort us, who are grieved and wearied with the burden of our sins.  Thy
                    property is always to have mercy; to thee only it appertaineth to forgive
                    sins.  Spare us therefore, good Lord, spare thy people, whom thou hast
                    redeemed; enter not into judgment with thy servants;  but so turn thine anger
                    from us, who meekly acknowledge our transgressions, and truly repent us of
                    our faults, and so make haste to help us in this world, that we may ever
                    live with thee in the world to come; through Jesus Christ our Lord.
                    <i>Amen.</i><br/>
                    <br/>
                    <br/>
                </b>

                <div class="text-center">
                    <small>
                        <i>Then shall the People say this that followeth, after the Minister.</i><br/>
                        <br/>
                    </small>
                </div>

                <b>
                    TURN thou us, O good Lord, and so shall we be turned.  Be favourable, O
                    Lord, Be favourable to thy people, Who turn to thee in weeping, fasting, and
                    praying.  For thou art a merciful God, Full of compassion, Long-suffering,
                    and of great pity.  Thou sparest when we deserve punishment, And in thy
                    wrath thinkest upon mercy.  Spare thy people, good Lord, spare them,
                    And let not thine heritage be brought to confusion.  Hear us, O Lord,
                    for thy mercy is great, And after the multitude of thy mercies look upon
                    us; Through the merits and mediation of thy blessed Son, Jesus Christ our
                    Lord.  Amen.<br/>
                    <br/>
                    <br/>
                </b>

                <div class="text-center">
                    <small>
                        <i>Then the Minister shall say,</i>
                    </small>
                </div>
                <br/>
                <br/>


                <b>
                    O GOD, whose nature and property is ever to have mercy and to forgive;
                    Receive our humble petitions; and though we be tied and bound with the chain
                    of our sins, yet let the pitifulness of thy great mercy loose us; for the
                    honour of Jesus Christ, our Mediator and Advocate. <i>Amen.</i><br/>
                    <br/>

                    THE LORD bless us, and keep us.  The LORD make his face to shine upon us,
                    and be gracious unto us.  The LORD lift up his countenance upon us, and
                    give us peace, both now and evermore. <i>Amen.</i><br/>
                </b>
            </blockquote>
